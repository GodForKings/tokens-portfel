export interface IToken {
	/**
	 * Символ торговой пары (например: "BTCUSDT", "ETHBTC")
	 * Формат: BASEQUOTE (базовая валюта/котируемая валюта)
	 */
	symbol: string

	/**
	 * Абсолютное изменение цены за последние 24 часа
	 * Рассчитывается как: (текущая цена - цена открытия 24 часа назад)
	 * Пример: "150.50" (положительное значение - рост, отрицательное - падение)
	 */
	priceChange: string

	/**
	 * Процентное изменение цены за последние 24 часа
	 * Рассчитывается как: (priceChange / цена открытия) * 100%
	 * Пример: "2.50" (означает +2.50%)
	 */
	priceChangePercent: string

	/**
	 * Средневзвешенная цена за последние 24 часа
	 * Рассчитывается с учетом объема торгов в каждой сделке
	 * Формула: ∑(цена * объем) / ∑(объем)
	 */
	weightedAvgPrice: string

	/**
	 * Цена закрытия предыдущего 24-часового периода
	 * Это цена, по которой торговалась пара 24 часа назад
	 */
	prevClosePrice: string

	/**
	 * Последняя цена, по которой была совершена сделка
	 * Это текущая рыночная цена торговой пары
	 */
	lastPrice: string

	/**
	 * Количество в последней сделке
	 * Объем последней совершенной сделки в базовой валюте
	 */
	lastQty: string

	/**
	 * Лучшая цена покупки (самая высокая цена, по которой кто-то готов купить)
	 * Это цена, по которой вы можете продать актив немедленно
	 */
	bidPrice: string

	/**
	 * Лучшая цена продажи (самая низкая цена, по которой кто-то готов продать)
	 * Это цена, по которой вы можете купить актив немедленно
	 */
	askPrice: string

	/**
	 * Цена открытия 24 часа назад
	 * Цена, с которой начался текущий 24-часовой период
	 */
	openPrice: string

	/**
	 * Самая высокая цена за последние 24 часа
	 * Максимальная цена, по которой торговалась пара за период
	 */
	highPrice: string

	/**
	 * Самая низкая цена за последние 24 часа
	 * Минимальная цена, по которой торговалась пара за период
	 */
	lowPrice: string

	/**
	 * Объем торгов базовой валютой за последние 24 часа
	 * Суммарное количество базовой валюты, торгуемой за период
	 * Пример: для BTCUSDT - объем в BTC
	 */
	volume: string

	/**
	 * Объем торгов котируемой валютой за последние 24 часа
	 * Суммарное количество котируемой валюты, торгуемой за период
	 * Рассчитывается как: объем * цена каждой сделки
	 * Пример: для BTCUSDT - объем в USDT
	 */
	quoteVolume: string

	/**
	 * Время открытия 24-часового окна
	 * UNIX timestamp в миллисекундах
	 * Начало периода, за который представлена статистика
	 */
	openTime: number

	/**
	 * Время закрытия 24-часового окна
	 * UNIX timestamp в миллисекундах
	 * Конец периода, за который представлена статистика
	 */
	closeTime: number

	/**
	 * ID первой сделки в 24-часовом окне
	 * Уникальный идентификатор самой ранней сделки за период
	 */
	firstId: number

	/**
	 * ID последней сделки в 24-часовом окне
	 * Уникальный идентификатор самой последней сделки за период
	 */
	lastId: number

	/**
	 * Общее количество сделок за последние 24 часа
	 * Количество всех совершенных сделок за период
	 */
	count: number
}

export interface ITokenWSS {
	s: string // "BTCUSDT"
	c: number // current price
}
